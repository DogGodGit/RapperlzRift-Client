using UnityEngine;
using System.Collections;

public class LoginWithGoogleNACommand : NativeApiCommand
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Constructors

	public LoginWithGoogleNACommand() : base("LoginWithGoogle")
	{
	}

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Member functions

	protected override NativeApiResponse CreateResponse()
	{
		return new LoginWithGoogleNAResponse();
	}
}

public class LoginWithGoogleNAResponse : NativeApiResponse
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Constants

	public const int kResult_Canceled = 101;

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Member variables

	private string	m_sGoogleUserId = null;

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Properties

	public string googleUserId
	{
		get { return m_sGoogleUserId; }
	}

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Member functions

	protected override void HandleBody()
	{
		base.HandleBody();

		m_sGoogleUserId = LitJsonUtil.GetStringProperty(m_joContent, "googleUserId");
	}
}
