using UnityEngine;
using System.Collections;

using LitJson;

public class StoreKitInitNACommand : NativeApiCommand
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Constructors

	public StoreKitInitNACommand()
		: base("StoreKitInit")
	{
	}

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Member variables

	string m_sPublicKey = null;

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Properties

	public string PublicKey
	{
		get { return m_sPublicKey; }
		set { m_sPublicKey = value; }
	}

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Member functions

	protected override NativeApiResponse CreateResponse()
	{
		return new StoreKitInitNAResponse();
	}

	protected override JsonData MakeRequestContent()
	{
		JsonData joReq = base.MakeRequestContent();

		joReq["publicKey"] = m_sPublicKey;
		
		return joReq;
	}
}

public class StoreKitInitNAResponse : NativeApiResponse
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Constants

    public const int kResult_Cancled = 101;

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Member variables

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Properties

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Member functions

	protected override void HandleBody()
	{
		base.HandleBody();

    }
}
