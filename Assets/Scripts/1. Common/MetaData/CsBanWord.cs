public class CsBanWord
{
	int m_nType;
	string m_strWord;

	//---------------------------------------------------------------------------------------------------
	public int Type
	{
		get { return m_nType; }
	}

	public string Word
	{
		get { return m_strWord; }
	}

	//---------------------------------------------------------------------------------------------------
	public CsBanWord(WPDBanWord banWord)
	{
		m_nType = banWord.type;
		m_strWord = banWord.word;
	}
}
